#include "pipex_types.h"

int	glue_child_pipes(int i, int *pipes[])
{
	if (dup2(pipes[i][0], 0) == -1 || dup2(pipes[i + 1][1], 1) == -1)
	{
		perror("Error dup2");
		return (1);
	}
	return (0);
}
